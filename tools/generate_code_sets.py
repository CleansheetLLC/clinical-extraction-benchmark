#!/usr/bin/env python3
"""Generate TypeScript ReadonlySet files from authoritative code system sources.

Inputs (download manually into tools/reference-data/):
  - ICD-10-CM: icd10cm_order_2026.txt from CMS (fixed-width)
  - LOINC:     Loinc.csv from loinc.org (CSV, LOINC_NUM column)
  - RxNorm:    RXNCONSO.RRF from NLM (pipe-delimited)

Usage:
  python generate_code_sets.py \\
    --icd10 reference-data/icd10cm_order_2026.txt \\
    --loinc reference-data/Loinc.csv \\
    --rxnorm reference-data/RXNCONSO.RRF \\
    --outdir ../../corporate/intranet/src/demo/data/generated/

Each flag is optional -- generates only the systems you have source files for.
"""

import argparse
import csv
import os
import sys
from pathlib import Path


def parse_icd10cm(filepath: str) -> list[str]:
    """Parse ICD-10-CM order file (fixed-width).

    Format: positions 6-12 contain the code (no decimal), position 14 is
    header flag (0=detail, 1=header). We include only detail codes and
    insert decimal after position 3 when code length > 3.
    """
    codes = []
    with open(filepath, "r", encoding="utf-8", errors="replace") as f:
        for line in f:
            if len(line) < 15:
                continue
            raw_code = line[6:13].strip()
            header_flag = line[14:15].strip()
            if header_flag == "1":
                continue  # skip header/category rows
            if not raw_code:
                continue
            # Insert decimal after 3rd character if longer than 3
            if len(raw_code) > 3:
                code = raw_code[:3] + "." + raw_code[3:]
            else:
                code = raw_code
            codes.append(code)
    return sorted(set(codes))


def parse_loinc(filepath: str) -> list[str]:
    """Parse LOINC CSV file, extracting LOINC_NUM column."""
    codes = set()
    with open(filepath, "r", encoding="utf-8", errors="replace") as f:
        reader = csv.DictReader(f)
        for row in reader:
            num = row.get("LOINC_NUM", "").strip()
            if num:
                codes.add(num)
    return sorted(codes)


def parse_rxnorm(filepath: str) -> list[str]:
    """Parse RXNCONSO.RRF (pipe-delimited), extracting unique RXCUIs where SAB=RXNORM."""
    cuis = set()
    with open(filepath, "r", encoding="utf-8", errors="replace") as f:
        for line in f:
            fields = line.split("|")
            if len(fields) < 12:
                continue
            sab = fields[11].strip()
            if sab != "RXNORM":
                continue
            rxcui = fields[0].strip()
            if rxcui:
                cuis.add(rxcui)
    return sorted(cuis)


def write_ts_set(filepath: str, var_name: str, codes: list[str]) -> None:
    """Write a TypeScript file exporting a ReadonlySet of codes."""
    os.makedirs(os.path.dirname(filepath), exist_ok=True)

    # Chunk into lines of ~120 chars for readability
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(f"// Auto-generated by generate_code_sets.py -- DO NOT EDIT\n")
        f.write(f"// {len(codes):,} codes\n")
        f.write(f"export const {var_name}: ReadonlySet<string> = new Set([\n")

        line = "  "
        for code in codes:
            entry = f'"{code}",'
            if len(line) + len(entry) + 1 > 120:
                f.write(line.rstrip() + "\n")
                line = "  "
            line += entry
        if line.strip():
            f.write(line.rstrip() + "\n")

        f.write("]);\n")


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate TypeScript code sets from reference data")
    parser.add_argument("--icd10", help="Path to icd10cm_order_2026.txt")
    parser.add_argument("--loinc", help="Path to Loinc.csv")
    parser.add_argument("--rxnorm", help="Path to RXNCONSO.RRF")
    parser.add_argument(
        "--outdir",
        default="../../corporate/intranet/src/demo/data/generated/",
        help="Output directory for .ts files",
    )
    args = parser.parse_args()

    if not any([args.icd10, args.loinc, args.rxnorm]):
        print("No input files specified. Use --icd10, --loinc, and/or --rxnorm.", file=sys.stderr)
        parser.print_help()
        sys.exit(1)

    outdir = Path(args.outdir)

    if args.icd10:
        print(f"Parsing ICD-10-CM from {args.icd10}...")
        codes = parse_icd10cm(args.icd10)
        out = outdir / "icd10cm-codes.ts"
        write_ts_set(str(out), "ICD10CM_CODES", codes)
        print(f"  -> {out} ({len(codes):,} codes)")

    if args.loinc:
        print(f"Parsing LOINC from {args.loinc}...")
        codes = parse_loinc(args.loinc)
        out = outdir / "loinc-codes.ts"
        write_ts_set(str(out), "LOINC_CODES", codes)
        print(f"  -> {out} ({len(codes):,} codes)")

    if args.rxnorm:
        print(f"Parsing RxNorm from {args.rxnorm}...")
        codes = parse_rxnorm(args.rxnorm)
        out = outdir / "rxnorm-codes.ts"
        write_ts_set(str(out), "RXNORM_CODES", codes)
        print(f"  -> {out} ({len(codes):,} codes)")

    print("Done.")


if __name__ == "__main__":
    main()
